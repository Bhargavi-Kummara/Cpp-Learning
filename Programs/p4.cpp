//  Write a program in C++ to check the upper and lower limits of integer.

#include <iostream>
#include <climits>
using namespace std;

int main()
{
    cout << "Check the upper and lower limits of integer :\n";
    cout << "---------------------------------------------\n";
    cout << "The maximum limit of int data type : " << INT_MAX << "\n";
    cout << "The minimum limit of int data type : " << INT_MIN << "\n";
    cout << "The maximum limit of unsigned int data type : " << UINT_MAX << "\n";
    cout << "The maximum limit of char data type : " << CHAR_MAX << "\n";
    cout << "The minimum limit of char data type : " << CHAR_MIN << "\n";
    cout << "The maximum limit of long long data type : " << LONG_LONG_MAX << "\n";
    cout << "The minimum limit of long long data type : " << LONG_LONG_MIN << "\n";
    cout << "The maximum limit of unsigned long long data type : " << ULONG_LONG_MAX << "\n";
    cout << "The maximum limit of signed char data type : " << SCHAR_MAX << "\n";
    cout << "The minimum limit of signed char data type : " << SCHAR_MIN << "\n";

}